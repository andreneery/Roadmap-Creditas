package calculadoraJUnit

class Calculadora {

    fun soma(x: Int, y: Int): Int {
        return x + y
    }

    fun subtracao(x: Int, y: Int): Int {
        return x - y
    }

    fun multiplicacao(x: Int, y: Int): Int {
        return x * y
    }

    fun divisao(x: Int, y: Int): Int {
        return x / y
    }
}