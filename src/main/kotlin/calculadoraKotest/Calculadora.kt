package calculadoraKotest

class Calculadora {

    fun soma(x: Int, y: Int): Int {
        return x + y
    }

    fun subtração(x: Int, y: Int): Int {
        return x - y
    }

    fun multiplicação(x: Int, y: Int): Int {
        return x * y
    }

    fun divisão(x: Int, y: Int): Int {
        return x / y
    }
}